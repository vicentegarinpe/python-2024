# Definir dos números complejos
complejo1 = 3 + 2j
complejo2 = 1 - 4j

# Sumar los números complejos
suma = complejo1 + complejo2
print("Suma:", suma)

# Restar los números complejos
resta = complejo1 - complejo2
print("Resta:", resta)

# Multiplicar los números complejos
multiplicacion = complejo1 * complejo2
print("Multiplicación:", multiplicacion)

# Dividir los números complejos
division = complejo1 / complejo2
print("División:", division)

# Obtener la parte real e imaginaria de un número complejo
parte_real = complejo1.real
parte_imaginaria = complejo1.imag
print("Parte Real:", parte_real)
print("Parte Imaginaria:", parte_imaginaria)

# Calcular el conjugado de un número complejo
conjugado = complejo1.conjugate()
print("Conjugado:", conjugado)

# Calcular el módulo de un número complejo
modulo = abs(complejo1)
print("Módulo:", modulo)
